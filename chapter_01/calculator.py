print(first_term + ' - ' + second_term + ' = ' + str(result))

result = int(first_term) * int(second_term)
print(first_term + ' * ' + second_term + ' = ' + str(result))

result = int(first_term) / int(second_term)
print(first_term + ' / ' + second_term + ' = ' + str(result))

result = int(first_term) ** int(second_term)
print(first_term + ' ^ ' + second_term + ' = ' + str(result))

result = int(first_term) % int(second_term)
